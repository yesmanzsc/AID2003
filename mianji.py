#计算梯形面积
#梯形面积 = (上底 + 下底) * 高 / 2

def calculation_of_trapezoidal_area (top_base,lower_bottom,height):
    """
    计算梯形面积
    :param top_base: 上底
    :param lower_bottom: 下底
    :param height: 高
    :return: 面积
    """
    return (top_base + lower_bottom) * height / 2


